// Helper function to translate string
function t(key, replacements = {}) {
    const lang = (window.store && window.store.state && window.store.state.language) || 'tr';
    let text = TRANSLATIONS[lang]?.[key] || TRANSLATIONS['tr']?.[key] || key;
    
    // Replace placeholders if any
    for (const [k, v] of Object.entries(replacements)) {
        text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    }
    return text;
}

// Function to translate static texts in DOM
function applyTranslations() {
    // 1. Text translations
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });

    // 2. Placeholders translations
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.setAttribute('placeholder', t(key));
    });

    // 3. Titles translations
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        el.setAttribute('title', t(key));
    });

    // 4. Update HTML lang attribute
    const lang = (window.store && window.store.state && window.store.state.language) || 'tr';
    document.documentElement.setAttribute('lang', lang);
}
